package com.letscode.Calculadora.Service;

public class Operacoes {
    public static float Soma(int numero1, int numero2){
        return numero1 + numero2;
    };

    public static float subtracao(int numero1, int numero2){
        return numero1 - numero2;
    };

    public static float multiplicacao(int numero1, int numero2){
        return numero1 * numero2;
    };

    public static float divisao(int numero1, int numero2){
        return (float) numero1 / numero2;
    };

}
